void C图形::f显示() {
	auto &v渲染控制 = m三维.fg渲染控制();
	v渲染控制.f清屏();
	//圆
	for (int i = 0; i != c圆数量; ++i) {
		C圆 &v圆 = m粒子管理.ma圆[i];
		m圆顶点数据[i] = v圆.fg顶点();
		m圆常量[i] = v圆.fg常量();
	}
	v渲染控制.f更新资源(m圆顶点缓冲.Get(), m圆顶点数据);
	v渲染控制.f更新资源(m常量缓冲1.Get(), m圆常量);
	v渲染控制.fs图形管线(*m圆管线);
	v渲染控制.fs图元拓扑(三维::E图元拓扑::e列表点);
	v渲染控制.fs顶点缓冲(m圆顶点缓冲.Get(), sizeof(S圆顶点));
	v渲染控制.f绘制(c圆顶点数);
	//光
	for (int i = 0; i != c光数量; ++i) {
		m光顶点数据[i * c光每顶点数] = m粒子管理.ma光[i].fg顶点(0, -1);
		m光顶点数据[i * c光每顶点数 + 1] = m粒子管理.ma光[i].fg顶点(0, 1);
		m光顶点数据[i * c光每顶点数 + 2] = m粒子管理.ma光[i].fg顶点(1, -1);
		m光顶点数据[i * c光每顶点数 + 3] = m粒子管理.ma光[i].fg顶点(1, 1);
	}
	v渲染控制.f更新资源(m光顶点缓冲.Get(), m光顶点数据);
	v渲染控制.fs图形管线(*m光管线);
	v渲染控制.fs图元拓扑(三维::E图元拓扑::e补丁4);
	v渲染控制.fs顶点缓冲(m光顶点缓冲.Get(), sizeof(S光顶点));
	v渲染控制.f绘制(c光顶点数);
	//结束
	v渲染控制.f显示();
}