#include <cflw图形_d3d着色器.h>
#include "图形.h"
#include "常量.h"
namespace 着色器 = cflw::图形::d3d着色器;
void C图形::f初始化(HWND a窗口) {
	m三维.f初始化(a窗口);
	auto &v渲染控制 = m三维.fg渲染控制();
	auto &v渲染状态 = m三维.fg渲染状态();
	auto &v缓冲工厂 = m三维.fg缓冲工厂();
	//圆
	三维::C顶点格式 v顶点格式;
	v顶点格式.f添加(三维::C顶点格式::e位置, 2);
	v顶点格式.f添加(三维::C顶点格式::e位置, 2);
	v顶点格式.f添加(三维::C顶点格式::e颜色, 4);
	着色器::C着色器工厂 v着色器工厂;
	v着色器工厂.f初始化();
	着色器::tp着色器 vs, hs, ds, gs, ps;
	v着色器工厂.f编译顶点着色器(vs, L"圆.hlsl", "vs");
	v着色器工厂.f编译几何着色器(gs, L"圆.hlsl", "gs");
	v着色器工厂.f编译像素着色器(ps, L"圆.hlsl", "ps");
	三维::S图形管线参数 v圆图形管线;
	v圆图形管线.fs顶点着色器(着色器::fc跨度(vs));
	v圆图形管线.fs几何着色器(着色器::fc跨度(gs));
	v圆图形管线.fs像素着色器(着色器::fc跨度(ps));
	v圆图形管线.fs输入布局(v顶点格式);
	v圆图形管线.fs光栅化(v渲染状态.m光栅化.m显示隐藏面.Get());
	v圆图形管线.fs混合(v渲染状态.m混合.m颜色叠加.Get());
	v圆图形管线.fs深度模板(v渲染状态.m深度模板.m正常深度r.Get());
	m三维.f创建图形管线(m圆管线, v圆图形管线);
	//光
	v着色器工厂.f编译顶点着色器(vs, L"光.hlsl", "vs");
	v着色器工厂.f编译外壳着色器(hs, L"光.hlsl", "hs");
	v着色器工厂.f编译域着色器(ds, L"光.hlsl", "ds");
	v着色器工厂.f编译像素着色器(ps, L"光.hlsl", "ps");
	三维::S图形管线参数 v光图形管线;
	v光图形管线.fs顶点着色器(着色器::fc跨度(vs));
	v光图形管线.fs外壳着色器(着色器::fc跨度(hs));
	v光图形管线.fs域着色器(着色器::fc跨度(ds));
	v光图形管线.fs像素着色器(着色器::fc跨度(ps));
	v光图形管线.fs输入布局(v顶点格式);
	v光图形管线.fs光栅化(v渲染状态.m光栅化.m显示隐藏面.Get());
	v光图形管线.fs混合(v渲染状态.m混合.m颜色叠加.Get());
	v光图形管线.fs深度模板(v渲染状态.m深度模板.m正常深度r.Get());
	m三维.f创建图形管线(m光管线, v光图形管线);
	//缓冲
	m窗口常量.m窗口大小 = {c基础范围x, c基础范围y};
	v缓冲工厂.f创建缓冲(m常量缓冲0, &m窗口常量, sizeof(m窗口常量), 三维::E缓冲::e常量);
	v渲染控制.fs常量缓冲(0, m常量缓冲0.Get());
	v缓冲工厂.f创建缓冲(m常量缓冲1, nullptr, sizeof(m圆常量), 三维::E缓冲::e常量);
	v渲染控制.fs常量缓冲(1, m常量缓冲1.Get());
	v缓冲工厂.f创建缓冲(m光顶点缓冲, m光顶点数据, sizeof(m光顶点数据), 三维::E缓冲::e顶点);
	v缓冲工厂.f创建缓冲(m圆顶点缓冲, m圆顶点数据, sizeof(m圆顶点数据), 三维::E缓冲::e顶点);
	//粒子
	m粒子管理.f初始化();
}
